# randoms.py

import random

# Line 1
print(random.randint(5, 20))  # Could be anything from 5 to 20 inclusive
# Line 2
print(random.randrange(3, 10, 2))  # Could be 3, 5, 7, or 9
# Line 3
print(random.uniform(2.5, 5.5))  # Float between 2.5 and 5.5

# Random number between 1 and 100
print(random.randint(1, 100))
